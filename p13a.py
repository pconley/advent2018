def turn_right(dir):
    if dir == UP : return RIGHT
    if dir == RIGHT : return DN
    if dir == DN : return LEFT
    if dir == LEFT : return UP
    raise ValueError

def turn_left(dir):
    if dir == UP : return LEFT
    if dir == LEFT : return DN
    if dir == DN : return RIGHT
    if dir == RIGHT : return UP
    raise ValueError

